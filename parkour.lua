      btnMin.Text          = "—"
      btnParar.Visible     = true
      div.Visible          = true
      scroll.Visible       = true
    end
  end)

  btnParar.MouseButton1Click:Connect(function()
    pararReproducao()
    atualizarStatus()
  end)
